#include "./libft/libft.h"

int		pwd(void)
{
	char		buff[4095];
	char		*cwd;

	if (!(cwd = getcwd(buff, 0)))
		return (-1);
	ft_putstr(cwd);
	ft_putchar('\n');
	return (1);
}
